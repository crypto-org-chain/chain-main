// Close will be called in graceful shutdown in start cmd
func (app *ChainApp) Close() error {
	err := app.BaseApp.Close()

	if cms, ok := app.CommitMultiStore().(*memiavlrootmulti.Store); ok {
		return stderrors.Join(err, cms.Close())
	}

	return err
}
